class ATmega2560:
    """
    Pin mappings for the ATmega2560 board.
    """
    #  arduino analog pins
    A0 = 0   # PF0 (ADC0)
    A1 = 1   # PF1 (ADC1)
    A2 = 2   # PF2 (ADC2)
    A3 = 3   # PF3 (ADC3)
    A4 = 4   # PF4 (ADC4)
    A5 = 5   # PF5 (ADC5)
    A6 = 6   # PF6 (ADC6)
    A7 = 7   # PF7 (ADC7)
    A8 = 8   # PK0 (ADC8 / PCINT16)
    A9 = 9   # PK1 (ADC9 / PCINT17)
    A10 = 10 # PK2 (ADC10 / PCINT18)
    A11 = 11 # PK3 (ADC11 / PCINT19)
    A12 = 12 # PK4 (ADC12 / PCINT20)
    A13 = 13 # PK5 (ADC13 / PCINT21)
    A14 = 14 # PK6 (ADC14 / PCINT22)
    A15 = 15 # PK7 (ADC15 / PCINT23)

    # arduino digital pins
    D0 = 16  # PE0 (RXD0 / PCINT8)
    D1 = 17  # PE1 (TXD0)
    D2 = 18  # PE4 (PWM / INT4)
    D3 = 19  # PE5 (PWM / INT5)
    D4 = 20  # PG5 (PWM)
    D5 = 21  # PE3 (PWM / AIN1)
    D6 = 22  # PH3 (OC4A)
    D7 = 23  # PH4 (OC4B)
    D8 = 24  # PH5 (OC4C)
    D9 = 25  # PH6 (OC2B)
    D10 = 26 # PB4 (PWM / PCINT4)
    D11 = 27 # PB5 (OC1A / PCINT5)
    D12 = 28 # PB6 (OC1B / PCINT6)
    D13 = 29 # PB7 (PWM / PCINT7 / LED)
    D14 = 30 # PJ1 (TXD3 / PCINT10)
    D15 = 31 # PJ0 (RXD3 / PCINT9)
    D16 = 32 # PH1 (TXD2)
    D17 = 33 # PH0 (RXD2)
    D18 = 34 # PD3 (TXD1 / INT3)
    D19 = 35 # PD2 (RXD1 / INT2)
    D20 = 36 # PD1 (SDA / INT1)
    D21 = 37 # PD0 (SCL / INT0)
    D22 = 38 # PA0 (AD0)
    D23 = 39 # PA1 (AD1)
    D24 = 40 # PA2 (AD2)
    D25 = 41 # PA3 (AD3)
    D26 = 42 # PA4 (AD4)
    D27 = 43 # PA5 (AD5)
    D28 = 44 # PA6 (AD6)
    D29 = 45 # PA7 (AD7)
    D30 = 46 # PC7 (A15)
    D31 = 47 # PC6 (A14)
    D32 = 48 # PC5 (A13)
    D33 = 49 # PC4 (A12)
    D34 = 50 # PC3 (A11)
    D35 = 51 # PC2 (A10)
    D36 = 52 # PC1 (A9)
    D37 = 53 # PC0 (A8)
    D38 = 54 # PD7 (T0)
    D39 = 55 # PG2 (ALE)
    D40 = 56 # PG1 (RD)
    D41 = 57 # PG0 (WR)
    D42 = 58 # PL7
    D43 = 59 # PL6
    D44 = 60 # PL5 (PWM)
    D45 = 61 # PL4 (PWM)
    D46 = 62 # PL3 (PWM)
    D47 = 63 # PL2 (T5)
    D48 = 64 # PL1 (ICP5)
    D49 = 65 # PL0 (ICP4)
    D50 = 66 # PB3 (MISO / PCINT3)
    D51 = 67 # PB2 (MOSI / PCINT2)
    D52 = 68 # PB1 (SCK / PCINT1)
    D53 = 69 # PB0 (SS / PCINT0)

    # helpful pins
    LED = D13 # PB7 (D13)
